<?php


$team = json_decode(file_get_contents('json/team.json'), true);
$profiles = json_decode(file_get_contents('json/profiles.json'), true);
$types = array('researcher'=>'Researchers','postdoc'=>'Postdocs','grad'=>'Graduate Students');
foreach($types as $type=>$label) {
  printf('<h3 class="fancy">%s</h3>', $label);
  echo '<div class="row">';
  foreach ($team as $member) {
    if ($member['type'] != $type) continue;
    $href_open = "";
    $href_close = "";
    foreach ($profiles as $profile) {
      if ($profile['name'] == $member['name']) {
        $href_open = '<a href="/profile/' . seoUrl($profile['name']) .'">';
        $href_close = '</a>';
      }
    }
    printf('<div class="col-md-6">
    <div class="row">
    <div class="col-md-5">%s%s%s</div>
    <div class="col-md-7"><small>%s</small></div>
    </div></div>', $href_open, $member['name'], $href_close, $member['affiliation']);
  }
  echo '</div>';
}
